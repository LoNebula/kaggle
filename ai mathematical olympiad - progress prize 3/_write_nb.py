    "        latest = out[0].outputs[0].text\n",
    "        conversation += latest\n",
    "        ans = extract_final_answer(latest)\n",
    "        if ans is not None:\n",
    "            return ans\n",
    "\n",
    "    return extract_final_answer(conversation) or ''\n",
