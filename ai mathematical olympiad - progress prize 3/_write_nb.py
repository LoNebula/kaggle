    "# ============================================================\n",
    "# Cell 7: Self-Consistency (Majority Voting)\n",
    "# ============================================================\n",
    "\n",
    "def majority_vote(answers: List[str]) -> str:\n",
    "    valid = [a for a in answers if a.strip()]\n",
    "    return Counter(valid).most_common(1)[0][0] if valid else '0'\n",
    "\n",
    "\n",
    "def solve_problem(problem: str, llm_instance, num_samples: int = NUM_SAMPLES) -> dict:\n",
    "    all_ans = tir_batch(problem, llm_instance, num_samples)\n",
    "    final   = majority_vote(all_ans)\n",
    "    return {\n",
    "        'answer': final,\n",
    "        'all_answers': all_ans,\n",
    "        'vote_distribution': dict(Counter(a for a in all_ans if a)),\n",
    "        'num_valid': sum(1 for a in all_ans if a),\n",
    "    }\n",
    "\n",
