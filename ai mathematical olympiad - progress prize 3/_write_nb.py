    "    conversation: str,\n",
    "    llm_instance,\n",
    "    max_rounds: int = MAX_TIR_ROUNDS,\n",
    ") -> str:\n",
    "    \"\"\"Execute code, feed back output, regenerate until \\\\boxed{} appears or rounds exhausted.\"\"\"\n",
    "    sp = SamplingParams(temperature=TEMPERATURE, top_p=TOP_P, max_tokens=MAX_NEW_TOKENS, n=1)\n",
    "    latest = conversation\n",
    "\n",
    "    for _ in range(max_rounds):\n",
    "        blocks = extract_code_blocks(latest)\n",
    "        if not blocks:\n",
    "            break\n",
    "        code_out = execute_python(blocks[-1])\n",
    "        conversation += (\n",
    "            f'\\n\\nExecution output:\\n```\\n{code_out}\\n```\\n\\n'\n",
    "            'Continue your solution:'\n",
